public class JavaArrays {
    public static void main(String [] args) {
        String [] cars = {"Volvo","BMW", "Ford", "Mazda"};
        System.out.println(cars);
        cars[0] = "Opel";
        System.out.println(cars[0]);
        System.out.println(cars[1]);
        System.out.println(cars[0+1]);
    }
}
